/**
 * Sanitize SVG content for safe rendering.
 * Removes scripts, event handlers, and other dangerous content.
 */
export function sanitizeSvg(html: string): string {
  return DOMPurify.sanitize(html, {
    USE_PROFILES: { svg: true },
    ALLOWED_TAGS: SVG_ALLOWED_TAGS,
    ALLOWED_ATTR: SVG_ALLOWED_ATTR,
    ADD_ATTR: ['xmlns', 'xmlns:xlink'],
  });
}

